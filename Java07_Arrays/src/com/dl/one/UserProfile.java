package com.dl.one;

class UserProfile {
	int userId;
	String userName;
	long contactNO;
	String city;
	public UserProfile(int userId, String userName, long contactNO, String city) {
		super();
		this.userId = userId;
		this.userName = userName;
		this.contactNO = contactNO;
		this.city = city;
	}
	

}